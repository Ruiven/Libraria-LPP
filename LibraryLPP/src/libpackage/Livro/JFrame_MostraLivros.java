    private void preencherTabela(){
        ArrayList<Livro> livro = vf.returnLivros();
        
        //Clear table
        for (int i=0; i<50; i++){
            tblivros.setValueAt("", i, 0);
            tblivros.setValueAt("", i, 1);
            tblivros.setValueAt("", i, 2);
            tblivros.setValueAt("", i, 3);
            tblivros.setValueAt("", i, 4);
        }
        
        //Populate table
        for (int i=0; i<livro.size(); i++){
            
        tblivros.setValueAt(livro.get(i).getNome(),i,0);
        tblivros.setValueAt(livro.get(i).getEditor(),i,1);
        tblivros.setValueAt(livro.get(i).getNomeCategoria(),i,2);
        tblivros.setValueAt(livro.get(i).getLancamento(),i,3);
        tblivros.setValueAt(livro.get(i).getEstado(),i,4);

        
        
    }
    }
